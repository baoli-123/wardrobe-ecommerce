package com.itheima.model;

public class Type {
    private int id;           //编号
    private String typeName;  //类别名称

    public Type() {
    }

    public Type(int id, String typeName) {
        this.id = id;
        this.typeName = typeName;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getTypeName() {
        return typeName;
    }

    public void setTypeName(String typeName) {
        this.typeName = typeName;
    }
}
